jQuery.fn.sort = function() {  
    return this.pushStack( [].sort.apply( this, arguments ), []);  
};  

 function sort_on_name(a,b){  
     if (a.name == b.name){
       return 0;
     }
     return a.l_name> b.l_name ? 1 : -1;  
 };  
  function sort_on_name_desc(a,b){  
     return sort_on_name(a,b) * -1;  
 };


